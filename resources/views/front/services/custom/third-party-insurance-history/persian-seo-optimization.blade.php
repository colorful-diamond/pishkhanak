<script>
// Persian input validation and enhancement
document.addEventListener('DOMContentLoaded', function() {
    // Convert English numbers to Persian
    function convertToPersianNumbers(str) {
        const persianDigits = '۰۱۲۳۴۵۶۷۸۹';
        const englishDigits = '0123456789';
        
        for (let i = 0; i < englishDigits.length; i++) {
            str = str.replace(new RegExp(englishDigits[i], 'g'), persianDigits[i]);
        }
        return str;
    }
    
    // Enhance Persian text elements
    const persianElements = document.querySelectorAll('.persian-text, .persian-numbers');
    persianElements.forEach(element => {
        if (element.dataset.convertNumbers === 'true') {
            element.textContent = convertToPersianNumbers(element.textContent);
        }
    });
    
    // Cultural accessibility enhancements
    const insuranceCards = document.querySelectorAll('.insurance-card');
    insuranceCards.forEach(card => {
        card.setAttribute('role', 'article');
        card.setAttribute('aria-label', 'کارت اطلاعات بیمه');
    });
    
    // Persian keyboard support
    const inputs = document.querySelectorAll('input[type="text"], textarea');
    inputs.forEach(input => {
        input.addEventListener('keydown', function(e) {
            // Enable Persian keyboard shortcuts
            if (e.ctrlKey && e.key === 'Shift') {
                // Toggle keyboard layout hint
                console.log('Persian keyboard layout active');
            }
        });
    });
});

// Insurance-specific enhancements
function enhanceInsuranceDisplay() {
    // Format insurance percentages in Persian
    const percentageElements = document.querySelectorAll('.insurance-percentage');
    percentageElements.forEach(element => {
        const value = element.textContent;
        if (value.includes('%')) {
            element.innerHTML = `<span class="persian-numbers" data-convert-numbers="true">${value}</span>`;
        }
    });
    
    // Add semantic meaning to insurance status
    const statusElements = document.querySelectorAll('.insurance-status');
    statusElements.forEach(element => {
        const status = element.textContent.trim();
        switch(status) {
            case 'فعال':
                element.classList.add('text-green-600', 'font-semibold');
                element.setAttribute('aria-label', 'بیمه نامه فعال');
                break;
            case 'منقضی':
                element.classList.add('text-red-600', 'font-semibold');
                element.setAttribute('aria-label', 'بیمه نامه منقضی شده');
                break;
            case 'تعلیق':
                element.classList.add('text-yellow-600', 'font-semibold');
                element.setAttribute('aria-label', 'بیمه نامه در وضعیت تعلیق');
                break;
        }
    });
}

// Initialize enhancements when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', enhanceInsuranceDisplay);
} else {
    enhanceInsuranceDisplay();
}
</script>
