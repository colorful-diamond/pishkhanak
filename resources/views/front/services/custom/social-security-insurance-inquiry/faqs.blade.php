    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const searchInput = document.getElementById('faq-search');
            const categoryBtns = document.querySelectorAll('.faq-category-btn');
            const faqItems = document.querySelectorAll('.faq-item');
            const faqQuestions = document.querySelectorAll('.faq-question');
            const resultsCounter = document.getElementById('results-count');
            const resultsDiv = document.getElementById('faq-results');

            // Search functionality
            searchInput.addEventListener('input', function() {
                const searchTerm = this.value.toLowerCase().trim();
                filterFAQs(searchTerm, getActiveCategory());
            });

            // Category filtering
            categoryBtns.forEach(btn => {
                btn.addEventListener('click', function() {
                    categoryBtns.forEach(b => b.classList.remove('active', 'bg-blue-600', 'text-white'));
                    categoryBtns.forEach(b => b.classList.add('bg-gray-100', 'text-gray-700'));
                    
                    this.classList.add('active', 'bg-blue-600', 'text-white');
                    this.classList.remove('bg-gray-100', 'text-gray-700');
                    
                    const category = this.getAttribute('data-category');
                    const searchTerm = searchInput.value.toLowerCase().trim();
                    filterFAQs(searchTerm, category);
                });
            });

            // FAQ toggle functionality
            faqQuestions.forEach(question => {
                question.addEventListener('click', function() {
                    const faqItem = this.parentElement;
                    const answer = faqItem.querySelector('.faq-answer');
                    const chevron = this.querySelector('.faq-chevron');
                    
                    if (answer.classList.contains('hidden')) {
                        answer.classList.remove('hidden');
                        answer.style.maxHeight = answer.scrollHeight + 'px';
                        chevron.style.transform = 'rotate(180deg)';
                    } else {
                        answer.classList.add('hidden');
                        answer.style.maxHeight = '0px';
                        chevron.style.transform = 'rotate(0deg)';
                    }
                });
            });

            function getActiveCategory() {
                const activeBtn = document.querySelector('.faq-category-btn.active');
                return activeBtn ? activeBtn.getAttribute('data-category') : 'all';
            }

            function filterFAQs(searchTerm, category) {
                let visibleCount = 0;
                
                faqItems.forEach(item => {
                    const itemCategory = item.getAttribute('data-category');
                    const itemKeywords = item.getAttribute('data-keywords') || '';
                    const itemText = item.textContent.toLowerCase();
                    
                    const categoryMatch = category === 'all' || itemCategory === category;
                    const searchMatch = searchTerm === '' || 
                                      itemText.includes(searchTerm) || 
                                      itemKeywords.toLowerCase().includes(searchTerm);
                    
                    if (categoryMatch && searchMatch) {
                        item.style.display = 'block';
                        visibleCount++;
                    } else {
                        item.style.display = 'none';
                    }
                });

                // Update results counter
                if (searchTerm !== '' || category !== 'all') {
                    resultsCounter.textContent = visibleCount;
                    resultsDiv.classList.remove('hidden');
                } else {
                    resultsDiv.classList.add('hidden');
                }

                // Show/hide category sections
                document.querySelectorAll('.faq-category').forEach(categorySection => {
                    const categoryItems = categorySection.querySelectorAll('.faq-item');
                    const visibleItems = Array.from(categoryItems).filter(item => item.style.display !== 'none');
                    
                    if (visibleItems.length > 0) {
                        categorySection.style.display = 'block';
                    } else {
                        categorySection.style.display = 'none';
                    }
                });
            }
        });
    </script>

