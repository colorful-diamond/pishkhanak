window.SecurityNoticeManager = securityManager;

// Auto-validate security on page load
document.addEventListener('DOMContentLoaded', () => {
    if (!securityManager.validateSecurityStatus()) {
        console.warn('Security validation failed - some features may be limited');
    }
});
