<script>
class SecurityNoticeManager {
    constructor() {
        this.initializeSecurity();
        this.monitorSecurity();
    }

    initializeSecurity() {
        // Check SSL connection
        if (location.protocol !== 'https:' && location.hostname !== 'localhost') {
            this.showSecurityWarning('اتصال غیرامن! لطفاً از HTTPS استفاده کنید.');
        }

        // Check for security headers
        this.checkSecurityHeaders();
        
        // Monitor console access
        this.preventConsoleAccess();
        
        // Add security event listeners
        this.addSecurityListeners();
    }

    checkSecurityHeaders() {
        // This would normally be done server-side
        // Here we just update the status indicator
        const statusIndicator = document.querySelector('.security-status');
        if (location.protocol === 'https:') {
            statusIndicator.querySelector('.bg-green-500').classList.add('animate-pulse');
        }
    }

    monitorSecurity() {
        // Monitor for suspicious activities
        let suspiciousActivity = 0;

        // Monitor rapid form submissions
        let lastSubmission = 0;
        document.addEventListener('submit', (e) => {
            const now = Date.now();
            if (now - lastSubmission < 1000) {
                suspiciousActivity++;
                if (suspiciousActivity > 3) {
                    this.triggerSecurityAlert('فعالیت مشکوک شناسایی شد');
                }
            }
            lastSubmission = now;
        });

        // Monitor console usage
        let consoleWarnings = 0;
        const originalConsole = console.log;
        console.log = function() {
            consoleWarnings++;
            if (consoleWarnings > 5) {
                // Don't trigger alert for legitimate debugging
                // Just log the activity
                originalConsole('Security: Console access monitored');
            }
            return originalConsole.apply(console, arguments);
        };
    }

    preventConsoleAccess() {
        // Warning for console access
        console.clear();
        console.log('%c🚨 هشدار امنیتی', 'color: red; font-size: 20px; font-weight: bold;');
        console.log('%cاستفاده از Console برای کاربران عادی ضروری نیست.', 'color: red; font-size: 14px;');
        console.log('%cاگر کسی از شما خواسته کدی را اینجا وارد کنید، احتمالاً قصد کلاهبرداری دارد.', 'color: red; font-size: 14px;');
        console.log('%cدر صورت مشاهده فعالیت مشکوک، با پشتیبانی تماس بگیرید: 021-12345678', 'color: blue; font-size: 12px;');
    }

    addSecurityListeners() {
        // Prevent right-click on sensitive elements
        document.querySelectorAll('input, .security-notice-container').forEach(element => {
            element.addEventListener('contextmenu', (e) => {
                e.preventDefault();
                this.showSecurityMessage('برای امنیت بیشتر، کلیک راست غیرفعال شده است.');
            });
        });

        // Monitor copy attempts on sensitive data
        document.addEventListener('copy', (e) => {
            const selection = window.getSelection().toString();
            if (selection.match(/\d{10}|\d{11}|bank|account/i)) {
                console.log('Security: Sensitive data copy detected');
            }
        });

        // Detect developer tools
        let devtools = {open: false, orientation: null};
        setInterval(() => {
            if (window.outerHeight - window.innerHeight > 200 || 
                window.outerWidth - window.innerWidth > 200) {
                if (!devtools.open) {
                    devtools.open = true;
                    console.log('🔍 Developer tools detected - Security monitoring active');
                }
            } else {
                devtools.open = false;
            }
        }, 500);
    }

    showSecurityWarning(message) {
        const warning = document.createElement('div');
        warning.className = 'security-warning fixed top-4 right-4 bg-red-100 border-2 border-red-500 text-red-800 px-6 py-4 rounded-lg shadow-lg z-50 max-w-sm';
        warning.innerHTML = `
            <div class="flex items-center space-x-2 space-x-reverse">
                <svg class="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 9v2m0 4h.01m-6.938 4h13.856c1.54 0 2.502-1.667 1.732-2.5L13.732 4c-.77-.833-1.964-.833-2.732 0L4.082 16.5c-.77.833.192 2.5 1.732 2.5z"></path>
                </svg>
                <div>
                    <div class="font-bold">هشدار امنیتی</div>
                    <div class="text-sm">${message}</div>
                </div>
            </div>
        `;

        document.body.appendChild(warning);

        setTimeout(() => {
            warning.remove();
        }, 10000);
    }

    showSecurityMessage(message) {
        const toast = document.createElement('div');
        toast.className = 'security-toast fixed top-20 right-4 bg-blue-100 border border-blue-300 text-blue-800 px-4 py-2 rounded-lg shadow-lg z-50 text-sm';
        toast.textContent = message;

        document.body.appendChild(toast);

        setTimeout(() => {
            toast.remove();
        }, 3000);
    }

    triggerSecurityAlert(reason) {
        console.warn('Security Alert:', reason);
        
        // Log security incident
        if (typeof gtag !== 'undefined') {
            gtag('event', 'security_alert', {
                event_category: 'Security',
                event_label: reason,
                value: 1
            });
        }

        // Show user-friendly message
        this.showSecurityWarning('فعالیت غیرعادی شناسایی شد. لطفاً صفحه را بازآوری کنید.');
    }

    // Public method to validate security status
    validateSecurityStatus() {
        const checks = {
            ssl: location.protocol === 'https:',
            domain: location.hostname.includes('pishkhanak.com') || location.hostname === 'localhost',
            userAgent: !navigator.userAgent.includes('bot'),
            cookies: navigator.cookieEnabled
        };

        const allPassed = Object.values(checks).every(check => check);
        
        if (!allPassed) {
            console.warn('Security validation failed:', checks);
            return false;
        }

        return true;
    }
}

