{{-- JavaScript for FAQ Functionality --}}
<script>
document.addEventListener('DOMContentLoaded', function() {
    // FAQ toggle functionality
    document.querySelectorAll('.faq-question').forEach(function(question) {
        question.addEventListener('click', function() {
            const answer = this.nextElementSibling;
            const isOpen = !answer.classList.contains('hidden');
            
            // Close all other FAQs
            document.querySelectorAll('.faq-answer').forEach(function(ans) {
                if (ans !== answer) {
                    ans.classList.add('hidden');
                }
            });
            
            // Toggle current FAQ
            if (isOpen) {
                answer.classList.add('hidden');
            } else {
                answer.classList.remove('hidden');
                // Smooth scroll to FAQ
                question.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        });
    });

    // Search functionality
    const searchInput = document.getElementById('faqSearch');
    const categorySelect = document.getElementById('faqCategory');
    const filterTags = document.querySelectorAll('.filter-tag');
    const faqItems = document.querySelectorAll('.faq-item');
    const resultCount = document.getElementById('resultCount');
    const totalCount = document.getElementById('totalCount');
    const searchStats = document.getElementById('searchStats');

    function filterFAQs() {
        const searchTerm = searchInput.value.toLowerCase();
        const selectedCategory = categorySelect.value;
        let visibleCount = 0;

        faqItems.forEach(function(item) {
            const question = item.querySelector('.faq-question h3').textContent.toLowerCase();
            const answer = item.querySelector('.faq-answer').textContent.toLowerCase();
            const category = item.dataset.category;
            const tags = item.dataset.tags || '';

            const matchesSearch = searchTerm === '' || 
                                question.includes(searchTerm) || 
                                answer.includes(searchTerm) ||
                                tags.includes(searchTerm);
            const matchesCategory = selectedCategory === 'all' || category === selectedCategory;

            if (matchesSearch && matchesCategory) {
                item.style.display = 'block';
                visibleCount++;
            } else {
                item.style.display = 'none';
            }
        });

        // Update search statistics
        if (searchTerm !== '' || selectedCategory !== 'all') {
            searchStats.classList.remove('hidden');
            resultCount.textContent = visibleCount;
            totalCount.textContent = faqItems.length;
        } else {
            searchStats.classList.add('hidden');
        }
    }

    // Event listeners for search and filter
    searchInput.addEventListener('input', filterFAQs);
    categorySelect.addEventListener('change', filterFAQs);

    // Filter tag functionality
    filterTags.forEach(function(tag) {
        tag.addEventListener('click', function() {
            const filter = this.dataset.filter;
            
            // Remove active class from all tags
            filterTags.forEach(function(t) {
                t.classList.remove('bg-blue-500', 'text-white');
                t.classList.add('bg-blue-100', 'text-blue-700');
            });
            
            // Add active class to clicked tag
            this.classList.remove('bg-blue-100', 'text-blue-700');
            this.classList.add('bg-blue-500', 'text-white');

            // Filter FAQs by tag
            faqItems.forEach(function(item) {
                const tags = item.dataset.tags || '';
                if (filter === 'all' || tags.includes(filter)) {
                    item.style.display = 'block';
                } else {
                    item.style.display = 'none';
                }
            });
        });
    });
});
</script>

