        <!-- JavaScript for FAQ functionality -->
        <script>
        document.addEventListener('DOMContentLoaded', function() {
            const searchInput = document.getElementById('faq-search');
            const categoryButtons = document.querySelectorAll('.faq-category-btn');
            const faqItems = document.querySelectorAll('.faq-item');
            const faqToggles = document.querySelectorAll('.faq-toggle');
            
            let currentCategory = 'all';

            // FAQ toggle functionality
            faqToggles.forEach(toggle => {
                toggle.addEventListener('click', function() {
                    const content = this.nextElementSibling;
                    const icon = this.querySelector('.faq-icon');
                    
                    // Close all other FAQs
                    faqToggles.forEach(otherToggle => {
                        if (otherToggle !== this) {
                            const otherContent = otherToggle.nextElementSibling;
                            const otherIcon = otherToggle.querySelector('.faq-icon');
                            if (otherContent) otherContent.classList.add('hidden');
                            if (otherIcon) otherIcon.style.transform = 'rotate(0deg)';
                        }
                    });
                    
                    // Toggle current FAQ
                    if (content) {
                        content.classList.toggle('hidden');
                        const isOpen = !content.classList.contains('hidden');
                        if (icon) icon.style.transform = isOpen ? 'rotate(180deg)' : 'rotate(0deg)';
                    }
                });
            });

            // Category filtering
            categoryButtons.forEach(button => {
                button.addEventListener('click', function() {
                    currentCategory = this.getAttribute('data-category');
                    
                    // Update button states
                    categoryButtons.forEach(btn => {
                        btn.classList.remove('active', 'bg-sky-600', 'text-white');
                        btn.classList.add('bg-gray-200', 'text-gray-700');
                    });
                    
                    this.classList.remove('bg-gray-200', 'text-gray-700');
                    this.classList.add('active', 'bg-sky-600', 'text-white');
                    
                    filterFAQs();
                });
            });

            // Search functionality
            searchInput.addEventListener('input', function() {
                filterFAQs();
            });

            function filterFAQs() {
                const searchTerm = searchInput.value.toLowerCase();
                
                faqItems.forEach(item => {
                    const question = item.querySelector('.faq-toggle span')?.textContent.toLowerCase() || '';
                    const answer = item.querySelector('.faq-content')?.textContent.toLowerCase() || '';
                    const itemCategory = item.getAttribute('data-category') || '';
                    
                    const categoryMatch = currentCategory === 'all' || itemCategory === currentCategory;
                    const searchMatch = searchTerm === '' || 
                        question.includes(searchTerm) || 
                        answer.includes(searchTerm);

                    if (categoryMatch && searchMatch) {
                        item.style.display = 'block';
                    } else {
                        item.style.display = 'none';
                    }
                });
            }
        });
        </script>
