<script>
document.addEventListener('DOMContentLoaded', function() {
    const searchInput = document.getElementById('faq-search');
    const categoryButtons = document.querySelectorAll('.faq-category-btn');
    const faqItems = document.querySelectorAll('.faq-item');
    const faqQuestions = document.querySelectorAll('.faq-question');
    const noResults = document.getElementById('no-results');
    const clearSearchBtn = document.getElementById('clear-search');
    const resultsCounter = document.getElementById('search-results-counter');
    const resultsCount = document.getElementById('results-count');
    
    let currentCategory = 'all';

    // Initialize FAQ toggle functionality
    faqQuestions.forEach(question => {
        question.addEventListener('click', function() {
            const answer = this.nextElementSibling;
            const icon = this.querySelector('.faq-icon');
            
            // Close all other FAQs
            faqQuestions.forEach(otherQuestion => {
                if (otherQuestion !== this) {
                    const otherAnswer = otherQuestion.nextElementSibling;
                    const otherIcon = otherQuestion.querySelector('.faq-icon');
                    if (otherAnswer) otherAnswer.classList.add('hidden');
                    if (otherIcon) otherIcon.style.transform = 'rotate(0deg)';
                }
            });
            
            // Toggle current FAQ
            if (answer) {
                answer.classList.toggle('hidden');
                const isOpen = !answer.classList.contains('hidden');
                if (icon) icon.style.transform = isOpen ? 'rotate(180deg)' : 'rotate(0deg)';
            }
        });
    });

    // Category button functionality
    categoryButtons.forEach(button => {
        button.addEventListener('click', function() {
            currentCategory = this.getAttribute('data-category');
            
            // Update button states
            categoryButtons.forEach(btn => {
                btn.classList.remove('active', 'bg-blue-600', 'text-white');
                btn.classList.add('bg-gray-100', 'text-gray-700');
            });
            
            this.classList.remove('bg-gray-100', 'text-gray-700');
            this.classList.add('active', 'bg-blue-600', 'text-white');
            
            performSearch();
        });
    });

    // Search functionality
    searchInput.addEventListener('input', function() {
        performSearch();
    });

    function performSearch() {
        const searchTerm = searchInput.value.toLowerCase();
        let visibleCount = 0;

        faqItems.forEach(item => {
            const questionText = item.querySelector('.faq-question span')?.textContent.toLowerCase() || '';
            const answerText = item.querySelector('.faq-answer')?.textContent.toLowerCase() || '';
            const itemCategory = item.closest('.faq-category')?.getAttribute('data-category') || '';
            const itemKeywords = item.getAttribute('data-keywords') || '';

            // Check category filter
            const categoryMatch = currentCategory === 'all' || itemCategory === currentCategory;
            
            // Check search term
            const searchMatch = searchTerm === '' || 
                itemKeywords.includes(searchTerm) || 
                questionText.includes(searchTerm) || 
                answerText.includes(searchTerm) ||
                removeArabicDiacritics(questionText).includes(removeArabicDiacritics(searchTerm)) ||
                removeArabicDiacritics(answerText).includes(removeArabicDiacritics(searchTerm));

            if (categoryMatch && searchMatch) {
                item.style.display = 'block';
                visibleCount++;
                
                // Highlight search terms
                if (searchTerm !== '') {
                    highlightText(item.querySelector('.faq-question span'), searchTerm);
                }
            } else {
                item.style.display = 'none';
            }
        });

        // Show/hide categories based on visible items
        document.querySelectorAll('.faq-category').forEach(category => {
            const visibleItems = category.querySelectorAll('.faq-item[style*="block"], .faq-item:not([style*="none"])');
            if (visibleItems.length === 0 && (searchTerm !== '' || currentCategory !== 'all')) {
                category.style.display = 'none';
            } else {
                category.style.display = 'block';
            }
        });

        // Update results counter
        if (searchTerm !== '') {
            resultsCounter.classList.remove('hidden');
            resultsCount.textContent = visibleCount;
        } else {
            resultsCounter.classList.add('hidden');
        }

        // Show/hide no results message
        if (visibleCount === 0 && (searchTerm !== '' || currentCategory !== 'all')) {
            noResults.style.display = 'block';
        } else {
            noResults.style.display = 'none';
        }
    }

    // Remove Persian/Arabic diacritics for better search
    function removeArabicDiacritics(text) {
        return text.replace(/[\u064B-\u0652\u0670\u0640]/g, '');
    }

    function highlightText(element, searchTerm) {
        if (!element || !searchTerm) return;
        
        const text = element.textContent;
        const regex = new RegExp(`(${searchTerm})`, 'gi');
        element.innerHTML = text.replace(regex, '<mark class="bg-yellow-200">$1</mark>');
    }

    // Global clear search function
    window.clearSearch = function() {
        searchInput.value = '';
        currentCategory = 'all';
        
        // Reset category buttons
        categoryButtons.forEach(btn => {
            btn.classList.remove('active', 'bg-blue-600', 'text-white');
            btn.classList.add('bg-gray-100', 'text-gray-700');
        });
        
        // Set "all" button as active
        const allButton = document.querySelector('[data-category="all"]');
        if (allButton) {
            allButton.classList.add('active', 'bg-blue-600', 'text-white');
            allButton.classList.remove('bg-gray-100', 'text-gray-700');
        }
        
        performSearch();
    };

    // Keyboard shortcuts
    document.addEventListener('keydown', function(e) {
        // Ctrl/Cmd + K to focus search
        if ((e.ctrlKey || e.metaKey) && e.key === 'k') {
            e.preventDefault();
            searchInput.focus();
        }
        
        // Escape to clear search
        if (e.key === 'Escape') {
            clearSearch();
        }
    });

    // Initialize search on page load
    performSearch();
});
</script>

