        progressBar.className = 'fixed top-0 left-0 w-full h-1 bg-gradient-to-r from-blue-500 to-purple-500 z-50 transform -translate-x-full transition-transform duration-300';
        document.body.appendChild(progressBar);

        window.addEventListener('scroll', () => {
            const scrolled = (window.scrollY / (document.documentElement.scrollHeight - window.innerHeight)) * 100;
            progressBar.style.transform = `translateX(-${100-scrolled}%)`;
        });
    };

    createScrollProgress();
});
</script>

